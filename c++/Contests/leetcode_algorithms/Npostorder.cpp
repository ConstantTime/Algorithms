class Solution {
public:
    vector < int > ans;
    void dfs(Node * root) {
        if(root == NULL) return ;
        for(auto j : root -> children) {
            dfs(j);
        }
        ans.push_back(root -> val);
    }
    vector<int> postorder(Node* root) {
        dfs(root);
        return ans;   
    }
};

